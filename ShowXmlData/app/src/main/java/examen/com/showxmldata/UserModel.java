package examen.com.showxmldata;

public class UserModel {

    private String name;
    private String email;
    private String gender;
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    String getEmail() {
        return email;
    }

    void setEmail(String email) {
        this.email = email;
    }

    String getGender() {
        return gender;
    }

    void setGender(String gender) {
        this.gender = gender;
    }

    int getAge() {
        return age;
    }

    void setAge(int age) {
        this.age = age;
    }

}
